#ifndef INCLUDED_ZENO_GRAPHICS_FONT_LOADER_HPP
#define INCLUDED_ZENO_GRAPHICS_FONT_LOADER_HPP

#include <zeno/Graphics/Glyph.hpp>

#include <string>

#include <ft2build.h>
#include FT_FREETYPE_H

namespace zeno {

class Image;

////////////////////////////////////////////////////////////
//
//	Class to load fonts to image glyphs
//
////////////////////////////////////////////////////////////
class FontLoader
{
public:
	////////////////////////////////////////////////////////////
	//
	//	Default constructor
	//
	////////////////////////////////////////////////////////////
	FontLoader(void);

	////////////////////////////////////////////////////////////
	//
	//	Destructor
	//
	////////////////////////////////////////////////////////////
	~FontLoader(void);

	////////////////////////////////////////////////////////////
	//
	//	Loads a font face given by the file path
	//
	////////////////////////////////////////////////////////////
	bool loadFont(const std::string& _filepath);

	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	bool loadGlyph(int _c, unsigned int _size);

	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	bool renderGlyph(Image& _image, Glyph& _glyph);

private:
	////////////////////////////////////////////////////////////
	//
	//	Freetype library handle
	//
	////////////////////////////////////////////////////////////
	FT_Library				m_Library;

	////////////////////////////////////////////////////////////
	//
	//	Freetype face handle
	//
	////////////////////////////////////////////////////////////
	FT_Face					m_Face;
};

} //~ namespace zeno

#endif //~ INCLUDED_ZENO_GRAPHICS_FONT_LOADER_HPP