#ifndef INCLUDED_ZENO_GRAPHICS_VERTEX_HPP
#define INCLUDED_ZENO_GRAPHICS_VERTEX_HPP

#include <zeno/Graphics/Colour.hpp>
#include <zeno/System/Vector2.hpp>
#include <zeno/System/Vector3.hpp>

namespace  zeno {

////////////////////////////////////////////////////////////
//
//	Simple class representing position, colour and texture
//	coordinates for a point.
//
////////////////////////////////////////////////////////////
class Vertex
{
public:
	////////////////////////////////////////////////////////////
	//
	//	Default constructor, positioned at origin, colour is 
	//	white and texture coordinates at origin
	//
	////////////////////////////////////////////////////////////
	Vertex(void);

	////////////////////////////////////////////////////////////
	//
	//	Constructor, position as given in parameter, colour is
	//	white and texture coordinates at origin
	//
	////////////////////////////////////////////////////////////
	Vertex(const Vector3f& _position);

	////////////////////////////////////////////////////////////
	//
	//	Constructor, position as given in parameter, colour as
	//	given in parameter and texture coordinates at origin
	//
	////////////////////////////////////////////////////////////
	Vertex(const Vector3f& _position, const Colour& _colour);

	////////////////////////////////////////////////////////////
	//
	//	Constructor, position as given in parameter, colour is
	//	white and texture coordinates as given in paremeter
	//
	////////////////////////////////////////////////////////////
	Vertex(const Vector3f& _position, const Vector2f& _texture);

	////////////////////////////////////////////////////////////
	//
	//	Constructor, position, colour and texture coordinates
	//	all as given in parameters
	//
	////////////////////////////////////////////////////////////
	Vertex(const Vector3f& _position, const Colour& _colour, const Vector2f& _texture);

public:
	////////////////////////////////////////////////////////////
	//
	//	Position of the vertex
	//
	////////////////////////////////////////////////////////////
	Vector3f			position;

	////////////////////////////////////////////////////////////
	//
	//	Colour of the vertex
	//
	////////////////////////////////////////////////////////////
	Colour				colour;

	////////////////////////////////////////////////////////////
	//
	//	2D vector specifying texture coordinates of the vertex
	//
	////////////////////////////////////////////////////////////
	Vector2f			texture;
};

} //~ namespace zeno

#endif //~ INCLUDED_ZENO_GRAPHICS_VERTEX_HPP