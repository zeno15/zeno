#ifndef INCLUDED_ZENO_GRAPHICS_TEXT_HPP
#define INCLUDED_ZENO_GRAPHICS_TEXT_HPP

#include <string>

#include <zeno/Graphics/Font.hpp>
#include <zeno/Graphics/Renderable.hpp>

#include <zeno/System/Transformable2D.hpp>

namespace zeno {

////////////////////////////////////////////////////////////
//
//	Class for drawing 2D text generated from a font
//
////////////////////////////////////////////////////////////
class Text : public Renderable, public Transformable2D
{
public:
	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	Text(void);
	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	Text(const std::string& _text, Font *_font, unsigned int _fontSize = 20);
	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	~Text(void);


	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	virtual void render(RenderData _data);
	
	////////////////////////////////////////////////////////////
	//
	//	Sets the text that will be renderd
	//
	////////////////////////////////////////////////////////////
	void setText(const std::string& _text);

	////////////////////////////////////////////////////////////
	//
	//	Sets the colour of the text
	//
	////////////////////////////////////////////////////////////
	void setColour(const Colour& _colour);

private:

private:
	std::vector<Vertex>			m_Verticies;

	////////////////////////////////////////////////////////////
	//
	//	Vertex Array Object for OpenGL drawing
	//
	////////////////////////////////////////////////////////////
	unsigned int				m_VAO;
	
	////////////////////////////////////////////////////////////
	//
	//
	//
	////////////////////////////////////////////////////////////
	Font *						m_Font;
	
	////////////////////////////////////////////////////////////
	//
	//	Colour of the text to be rendered
	//
	////////////////////////////////////////////////////////////
	Colour						m_Colour;
};

} //~ namespace zeno

#endif //~ INCLUDED_ZENO_GRAPHICS_TEXT_HPP